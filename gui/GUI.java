    private boolean check_pass(String pass) {
        if (pass.length() < 8) {
            JOptionPane.showMessageDialog(null, "Password is less than 8 characters", "Unsuccesful registration", JOptionPane.PLAIN_MESSAGE);
            return false;
        }
        boolean upper = false;
        boolean lower = false;
        boolean symbol = false;
        boolean number = false;
        char c;
        for (int i = 0; i < pass.length(); i++) {
            c = pass.charAt(i);
            if (Character.isDigit(c)) {
                number = true;
            } else {
                if (Character.isUpperCase(c)) {
                    upper = true;
                } else {
                    if (Character.isLowerCase(c)) {
                        lower = true;
                    } else {
                        symbol = true;
                    }
                }
            }
        }
        if (upper == false) {
            JOptionPane.showMessageDialog(null, "Password must contain at least one Uppercase letter", "Unsuccesful registration", JOptionPane.PLAIN_MESSAGE);
            return false;
        }
        if (lower == false) {
            JOptionPane.showMessageDialog(null, "Password must contain at least one Lowercase letter", "Unsuccesful registration", JOptionPane.PLAIN_MESSAGE);
            return false;
        }
        if (number == false) {
            JOptionPane.showMessageDialog(null, "Password must contain at least one Number", "Unsuccesful registration", JOptionPane.PLAIN_MESSAGE);
            return false;
        }
        if (symbol == false) {
            JOptionPane.showMessageDialog(null, "Password must contain at least one Symbol", "Unsuccesful registration", JOptionPane.PLAIN_MESSAGE);
            return false;
        }
        return true;

    }
