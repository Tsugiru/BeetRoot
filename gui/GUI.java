    private void passRegisterCheck(String a) {
        jSeparator5.setBackground(Color.green);
        if (a.length() < 8) {
            label_8characters.setForeground(Color.RED);
            jSeparator5.setBackground(Color.red);
        } else {
            label_8characters.setForeground(Color.GREEN);
        }

        boolean uppercase = false;
        boolean lowercase = false;
        boolean symboll = false;
        boolean numberr = false;
        char cc;
        for (int i = 0; i < a.length(); i++) {
            cc = a.charAt(i);
            if (Character.isDigit(cc)) {
                numberr = true;
            } else if (Character.isUpperCase(cc)) {
                uppercase = true;
            } else if (Character.isLowerCase(cc)) {
                lowercase = true;
            } else {
                symboll = true;
            }

        }
    
        if(uppercase==false)
        {
            label_uppercase.setForeground(Color.red);
            jSeparator5.setBackground(Color.red);
        }
        else
            label_uppercase.setForeground(Color.green);
        if(lowercase==false)
        {
             label_lowercase.setForeground(Color.red);
            jSeparator5.setBackground(Color.red);
        }
        else
            label_lowercase.setForeground(Color.green);
        if(numberr==false)
        {
         label_digit.setForeground(Color.red);
        jSeparator5.setBackground(Color.red);
        }
        else
            label_digit.setForeground(Color.green);
        if(symboll==false)
        {
            label_symbol.setForeground(Color.RED);
            jSeparator5.setBackground(Color.red);
        }
        else
            label_symbol.setForeground(Color.green);
    
    }
